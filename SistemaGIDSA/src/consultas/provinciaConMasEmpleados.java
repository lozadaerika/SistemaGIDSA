       void clienteMasCompra(){
        conexion cc = new conexion();
                Connection cn = cc.conectar("JAVY");      
                int contar=0;
                String nombre="";
                String sql = "";
                sql="select  NOM_PRO \n" +
                    "from EMPLEADOS  \n" +
                    "group by NOM_PRO \n" +
                    "HAVING count(NOM_PRO) =(SELECT  MAX(codigo) from \n" +
                    "   (select NOM_PRO as nombre, COUNT(NOM_PRO) as  codigo \n" +
                    "    from EMPLEADOS  \n" +
                    "    group by NOM_PRO) T)";

                try {
                    Statement psd  = cn.createStatement();
                    ResultSet rs = psd.executeQuery(sql);                           
                    while(rs.next()){
                       
                        nombre = rs.getString("NOM_PRO");
                    }
                           txtCodigo.setText(nombre);
                           //txtNombre.setText(cargarCliente(txtCodigo.getText()));
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex);  
                }
    }
    
       
       String cargarCliente(String dato){
        try{
            conexion cc = new conexion();
            Connection cn = cc.conectar("javy");
            String sql="";
            sql= "select NOM_PRO\n" +
                "from PROVINCIA cg\n" +
                "where NOM_PRO = "+dato;
            
            Statement psd = cn.createStatement();
            ResultSet rs = psd.executeQuery(sql);
            while(rs.next()){
                return rs.getString("NOM_PRO");
            }
            
        }catch(Exception ex){
         JOptionPane.showMessageDialog(null, ex);
        }
        return null;
    }
    
     

